function love.draw()
    for y=0, boardHeight do
        for x=0, boardWidth do
            love.graphics.setColor(255,255,255)
            local boardPartSize = 32
            local boardPartDrawSize = boardPartSize -3
            love.graphics.rectangle("fill",(x)*boardPartSize,(y)*boardPartSize,boardPartDrawSize,boardPartDrawSize)
        end
    end
end